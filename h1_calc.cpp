#include <iostream>
using namespace std;

int main()
{
	int n;
	int m;
	cin >> n >> m;

	cout << n << " + " << m << " = " << n + m << endl;
	cout << n << " - " << m << " = " << n - m << endl;
	cout << n << " * " << m << " = " << n * m << endl;
	cout << n << " / " << m << " = " << n / m << endl;
	cout << n << " % " << m << " = " << n % m << endl;	
}
