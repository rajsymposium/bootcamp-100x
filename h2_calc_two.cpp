#include <iostream>
using namespace std;

int main()
{
	long n;
	long m;
	cin >> n;
	cin >> m;

	cout << n << " + " << m << " = " << n + m << endl << endl;
	cout << n << " - " << m << " = " << n - m << endl << endl;
	cout << n << " * " << m << " = " << n * m << endl << endl;
	cout << n << " / " << m << " = " << n / m << endl << endl;
	cout << n << " % " << m << " = " << n % m << endl;	
}
